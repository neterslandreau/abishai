<h4>Data Retrieval:</h4>

	<p>
		To retrieve all accounts associated with a particular email address, locate the user by entering the email address including the "@" symbol, i.e. <em>roseline.seweje@lennar.com</em>.
	</p>

	<p>
		To retrieve a specific account, locate the account by entering the account login preceded by the "/" character, i.e. <em>/TrainingTR2.CCRep1</em> (The <em>LENNAR</em> is assumed).
	</p>

	<p>
		In either case, you will be presented with a screen similar to:<br>
		<img src="img/many_accounts.png" width="550"><br>
		If the user is not found, you will recieve notification below the form.
	</p>

<h4>Update Account Privileges</h4>

	<p>
		Account privileges determine the administrative level of the user with regards to the ability to modify homesite values.
	</p>

	<ul>
		<li>Superuser can force a homesite to be for sale, update the stage, and update the homesite.</li>
		<li>Stage user can update the stage and the homesite.</li>
	</ul>

	<p>
		Select the appropriate value from the drop-down menu for the user as desired and press the Update button. You will be notified if the operation was successful, or not, and will see the value for the Superuser change to reflect that.
	</p>
	
<h4>Update Account Status</h4>

	<p>
		The status of an account determines whether or not an account is active and is able to login. Use the procedure outlined above for account privileges.
	</p>
	<p>
		The status change <em>may</em> be overwritten at night if the status has not yet been updated by Roseline's group.
	</p>
